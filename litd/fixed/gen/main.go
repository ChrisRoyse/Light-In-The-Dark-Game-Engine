// Command gen writes trigtable.go (quarter-wave sine) and exptable.go (the
// log2-mantissa and exp2-fraction tables) for litd/fixed. Generation uses Go's
// math.* but the OUTPUT is committed, so runtime behavior is bit-identical across
// architectures by construction (integer table lookups, no runtime math.*).
// Regenerate with `go generate ./litd/fixed` — the diff must be empty.
package main

import (
	"bytes"
	"fmt"
	"math"
	"os"
)

const steps = 16384 // quarter-wave resolution: 65536 / 4

// expSteps is the resolution of the log2/exp2 fractional tables. 4096 entries
// with linear interpolation gives ~1e-8 relative accuracy — coarser than
// float64, deterministic by construction (D-2026-06-19-1).
const expSteps = 4096

func main() {
	genTrig()
	genExp()
}

func genTrig() {
	var b bytes.Buffer
	header(&b)
	fmt.Fprintf(&b, "const quarterSteps = %d\n\n", steps)
	fmt.Fprintf(&b, "// sinQuarter[i] = round(sin(i/%d * π/2) * 2^32), i in [0, %d].\n", steps, steps)
	fmt.Fprintln(&b, "var sinQuarter = [quarterSteps + 1]F64{")
	for i := 0; i <= steps; i++ {
		v := math.Sin(float64(i) / float64(steps) * math.Pi / 2)
		raw := int64(math.RoundToEven(v * 4294967296.0))
		fmt.Fprintf(&b, "\t%d,\n", raw)
	}
	fmt.Fprintln(&b, "}")
	write("trigtable.go", b.Bytes())
}

func genExp() {
	var b bytes.Buffer
	header(&b)
	fmt.Fprintf(&b, "const expSteps = %d\n\n", expSteps)
	// Committed 32.32 constants for base conversion (round-to-even of c * 2^32).
	c := func(v float64) int64 { return int64(math.RoundToEven(v * 4294967296.0)) }
	fmt.Fprintln(&b, "const (")
	fmt.Fprintf(&b, "\tln2F64     F64 = %d // ln(2)    * 2^32\n", c(math.Ln2))
	fmt.Fprintf(&b, "\tlog2eF64   F64 = %d // log2(e)  * 2^32\n", c(math.Log2E))
	fmt.Fprintf(&b, "\tlog10_2F64 F64 = %d // log10(2) * 2^32\n", c(math.Log10(2)))
	fmt.Fprintln(&b, ")")
	fmt.Fprintln(&b, "")
	// log2Mant[i] = round(log2(1 + i/expSteps) * 2^32), i in [0, expSteps];
	// log2 of a mantissa in [1,2) -> [0,1) as 32.32.
	fmt.Fprintf(&b, "// log2Mant[i] = round(log2(1 + i/%d) * 2^32), i in [0, %d].\n", expSteps, expSteps)
	fmt.Fprintln(&b, "var log2Mant = [expSteps + 1]F64{")
	for i := 0; i <= expSteps; i++ {
		v := math.Log2(1 + float64(i)/float64(expSteps))
		raw := int64(math.RoundToEven(v * 4294967296.0))
		fmt.Fprintf(&b, "\t%d,\n", raw)
	}
	fmt.Fprintln(&b, "}")
	fmt.Fprintln(&b, "")
	// exp2Frac[i] = round(2^(i/expSteps) * 2^32), i in [0, expSteps];
	// 2^f for f in [0,1) -> [1,2) as 32.32.
	fmt.Fprintf(&b, "// exp2Frac[i] = round(2^(i/%d) * 2^32), i in [0, %d].\n", expSteps, expSteps)
	fmt.Fprintln(&b, "var exp2Frac = [expSteps + 1]F64{")
	for i := 0; i <= expSteps; i++ {
		v := math.Exp2(float64(i) / float64(expSteps))
		raw := int64(math.RoundToEven(v * 4294967296.0))
		fmt.Fprintf(&b, "\t%d,\n", raw)
	}
	fmt.Fprintln(&b, "}")
	write("exptable.go", b.Bytes())
}

func header(b *bytes.Buffer) {
	fmt.Fprintln(b, "// Code generated by litd/fixed/gen. DO NOT EDIT.")
	fmt.Fprintln(b, "// Regenerate: go generate ./litd/fixed (diff must be empty).")
	fmt.Fprintln(b, "")
	fmt.Fprintln(b, "package fixed")
	fmt.Fprintln(b, "")
}

func write(name string, data []byte) {
	if err := os.WriteFile(name, data, 0o644); err != nil {
		panic(err)
	}
}
